#include "engine.hpp"
//-----------------------------------------------------------------------------
using namespace ng;
//-----------------------------------------------------------------------------
Icon::Icon(std::string src)
{
	if (!setIcon(src))
		kernel.print("ErrorIcon " + src, NORM);
}
//-----------------------------------------------------------------------------
bool Icon::setIcon(std::string src)
{
	if (!loadFromFile(src)) return 0;
		return 1;
}
